              <h1>2017</h1>

              <li>
                <a href="http://connectpa.co.uk/the-women-and-work-all-party-parliamentary-group/" target="_blank">The Women and Work All Party Parliamentary Group</a> brings together Parliamentarians, businesses, stakeholders and other interested groups to discuss and promote good practice around women in the workplace.  In January 2017, the Women and Work APPG launched its first annual report : Women Returners, a key recommendation of which was for employers to invest in programmes of support for returning parents. Take a look at the APPG report <a href="http://connectpa.co.uk/wp-content/uploads/2017/01/Women-and-work-Annual-report-low-res.pdf" target="_blank">here</a>
              </li>

              <li>
                For a number of years, McKinsey have been conducting global research into the value of gender diversity in the workplace. McKinsey’s 2012 report <a href="http://www.mckinsey.com/global-locations/europe-and-middleeast/france/en/latest-thinking/women-matter" target="_blank">‘Women Matter’</a> benchmarked the diversity programmes of 235 European companies indicating the best performing companies had a ‘critical mass of initiatives’ in place.  Within this list of initiatives were programmes ‘to smooth transition before, during and after maternity leave’
              </li>

              <li>
                McKinsey’s 2015 report ‘Diversity Matters’ analysed 366 public companies across a range of industries in the United Kingdom, Canada, the United States, and Latin America and found ‘a statistically significant relationship between a more diverse leadership team and better financial performance.’ <a href="http://www.mckinsey.com/global-themes/women-matter" target="_blank">Click here</a>
              for the McKinsey summary pages and many more related articles.  NB In order to access / download some articles, you may need to register!
              </li>

