                The Davis report into gender diversity in UK boardrooms, focused on the need to increase the number of women at executive level and the pipeline of future board members. <a href="https://www.gov.uk/government/publications/women-on-boards-5-year-summary-davies-review" target="_blank">Click here</a> for the Lord Davis 2015 summary report</li>

