              <h1>2018</h1>

              <h3>Working Forward</h3>

              <li>
                The Equalities and Human Rights Commission estimates maternity discrimination results in 54000 women losing their job each year in the UK.  This led to them setting up <a href="https://www.equalityhumanrights.com/en/pregnancy-and-maternity-workplace/working-forward" target="_blank">Working Forward</a> a collaboration of employers and industry bodies aiming to make UK workplaces the best they can be for pregnant women and new parents. Their research (published February 2018) accuses British employers of ‘living in the dark ages’ with ‘worrying attitudes’ and ‘unlawful behaviour when it comes to recruiting women’ – in particular Small Medium Enterprises.  Click <a href="https://www.equalityhumanrights.com/en/our-work/news/employers-dark-ages-over-recruitment-pregnant-women-and-new-mothers" target="_blank">here</a> for a link to their findings.
              </li>

              <br />

              <h3>Modern Families Index 2018</h3>

              <li>
                <a href="https://www.workingfamilies.org.uk/" target="_blank"> Working Families</a> is an established charity helping working parents, carers and employers to balance work and family.  In January 2018 they released their latest Employers Report based on a survey of 2750 working parents with children under 13.  Click <a href="docs/Modern Families Index 2018.pdf" target="_blank"> here</a> to find the summary report which shares results and commentary relating to the realities of flexible working, the impact of working hours culture, the ‘parenthood’ penalty in relation to careers and what employers should be focusing on to support families at work.

                <a href="https://www.workingfamilies.org.uk/research-publications/" target="_blank"> https://www.workingfamilies.org.uk/research-publications/</a>
              </li>

              <br />

              <h3>Female FTSE Board Report</h3>

              <li>
                I had the opportunity to hear Professor Susan Vinnicombe from Cranfield School of Management speak on International Women’s Day a number of years ago when I was working for Asda Walmart.  Her keynote had a remarkable impact on me and I continue to quote her words in conversation with returners.  Cranfield School of Management offers a wealth of resources and has been influencing and tracking the progress of women in businesses since 1999.  Click <a href="docs/Cranfield Female FTSE report 2017.pdf" target="_blank"> here</a> for more information and the latest Female FTSE Board report 2017.

                <a href="https://www.cranfield.ac.uk/som/expertise/changing-world-of-work/gender-and-leadership/female-ftse-index" target="_blank">
                https://www.cranfield.ac.uk/som/expertise/changing-world-of-work/gender-and-leadership/female-ftse-index</a>
              </li>

